<h1>Features</h1>

<div class="feature-list">
    <div class="feature">
        <h2>File formats</h2>
        <div class="feature-body">
            You can keep track and watch any file format as long as it is within the file size limit of your subscription plan. However, the comparison feature is limited to certain file types.
        </div>
    </div>

    <div class="feature">
        <h2>Notifications</h2>
        <div class="feature-body">
            Receive notifications and change reports in the application or as email to always stay on top of your updates. Depending on your plan you may also share these with unregistered users.
        </div>
    </div>
</div>

<div class="feature-list">
    <div class="feature">
        <h2>Comparison</h2>
        <div class="feature-body">
            Compare updates to websites, text files, word files, PDFs and images to quickly see what got changed. Other file formats can also be tracked but cannot be compared to previous versions.
        </div>
    </div>

    <div class="feature">
        <h2>History</h2>
        <div class="feature-body">
            Depending on your subscription plan you can compare changes between different versions making it possible to track multiple changes and comparing these changes with each other.
        </div>
    </div>
</div>

<div class="feature-list">
    <div class="feature">
        <h2>Subsections</h2>
        <div class="feature-body">
            Instead of watching and tracking a complete online resource for changes you can also specify specific elements or subsections e.g. a fixed PDF page, a specific area on a website etc.
        </div>
    </div>

    <div class="feature">
        <h2>Local/Offline</h2>
        <div class="feature-body">
            In addition to the online service we also offer a local version which can be used for single workstations (Windows 7+ only) or in a server environment with multiple users.
        </div>
    </div>
</div>